<p>A managed care plan where services are covered only if you go to doctors, specialists, or hospitals in the plan’s network (except in an emergency).</p>
<p>The EPO Plan is a consumer driven health plan with a Health Reimbursement Account offering members a different approach to how you pay for today’s health care with an opportunity to save for your future health care expenses. It is a lower-premium, high-deductible health insurance plan, which means you pay less out of your paycheck for premiums and more out of pocket at the point of service before the plan pays for services that are not considered preventive. Members who enroll in the EPO plan will be provided with an employer paid Health Reimbursement Account to help pay for some of these out-of-pocket medical expenses including deductibles, copays and other healthcare expenses.</p>
<div class="textbox">
<h1><B>What is an EPO Plan?</B></h1>
<p>An Exclusive Provider Organization (EPO) is a health plan that offers a large, national network of doctors and hospitals for you to choose from. However, if you choose out-of-network health care providers, it usually will not be covered.</p>
<h2><B>If you choose an EPO health plan, it’s important to know:</B></h2>
<ul>
  <li>We recommend you choose a primary care provider (PCP) to act as your personal health advocate and coordinate your health care, but it is not required.</li>
  <li>If you get medical care outside of your plan’s network (such as a doctor, hospital, clinic or pharmacy that is not a Cigna EPO provider), those out-of-network services are typically not covered under your EPO health plan.</li>
  <li>In an emergency, you will be covered. However, non-emergency hospital stays other than maternity stays and some outpatient procedures must be approved in advance.</li>
</ul>
<h2><B>Cost and benefits of EPO health insurance plans</B></h2>
<p>EPO health insurance plans typically cost you less than Health Maintenance Organization (HMO) and Preferred Provider Organization (PPO) plans with lower rates for care.</p>
<p>The downside of this type of plan is that any provider outside of the EPO network isn’t covered by your insurance. But because of these limitations, EPO plans generally can be suited well to individuals who don’t anticipate needing a great deal of medical care and want to save money.</p>
