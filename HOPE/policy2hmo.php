<h1><B>How an HMO Works</B></h1>
<p>An HMO is an organized public or private entity that provides basic and supplemental health services to its subscribers. The organization secures its network of health providers by entering into contracts with primary care physicians, clinical facilities, and specialists. The medical entities that enter into contracts with the HMO are paid an agreed-upon fee to offer a range of services to the HMOâ€™s subscribers. The agreed payment allows an HMO to offer lower premiums than other types of health insurance plans while retaining a high quality of care from its network.</p>
<h2><B>Advantages</B></h2>
<p>The first and most obvious advantage of participating in an HMO is the low cost. You'll pay fixed premiums on a monthly or annual basis that are lower than traditional forms of health insurance. These plans tend to come with low or no deductibles and your co-pays are generally lower than other plans. Your out-of-pocket costs will also be lower for your prescription.Billing also tends to be less complicated for those with an HMO.</p>
<p>There's also a very good likelihood that you'll have to deal with the insurer itself. That's because you have a primary care doctor you must choose from who is responsible to manage your treatment and care. This professional will also advocate for services on your behalf.

 This includes making referrals for specialty services for you.</p>
<p>The quality of care is generally higher with an HMO. The reason is that patients are encouraged to get annual physicals and to seek out treatment early.</p>
